            </p>
            <h2 class="text-yellow text-center text-2xl font-bold mb-4 mt-12">'Ik lust dat niet!'</h2>
            
            <p class="my-2 font-bold">                          
                Voor sommige leden is het eten op kamp het hoogtepunt van de dag, voor andere is het een echt drama.
                Vroeg of laat krijg je als leiding weleens te maken met een huilende ribbel die niets lust of een opstandige keti die weigert te eten. 
                Maar wat doe je het best in zo’n situatie? Wij vroegen raad aan kinderpsychologe Liesbeth Desmet van Praktijk Mobiel in Gent.
            </p>

            <h2 class="text-yellow text-center text-2xl font-bold mb-4 mt-12">Lust je het niet of is er meer aan de hand?</h2>
                        
            <p class="my-2">
                Wil je lid niet eten, knoop er dan een gesprekje mee aan. 
                Niet belerend, maar vragend: probeer te achterhalen wat het probleem precies is. 
                Het is best mogelijk dat het kind een bepaald gerecht simpelweg echt niet lust. 
                Soms zeggen kinderen ook dat ze iets niet lusten omdat ze het nog nooit geproefd hebben. 
                Ouders hebben het tegenwoordig vaak zo druk dat ze ‘s avonds de energie niet meer vinden om kinderen tegen hun zin iets te laten opeten. 
                ‘Je gaat niet van tafel voor je bord leeg is’ wordt meer en meer vervangen door een toegeeflijker variant. 
                Op kamp krijgen ze dingen op hun bord die ze misschien nog nooit gegeten hebben. 
                Laat hen er dan zeker eens van proeven – wie weet vinden ze het wel lekker?
            </p>

            <p class="my-2">
                In sommige gevallen is er effectief een onderliggende psychologische oorzaak, 
                zoals heimwee. Kinderen met heimwee voelen zich niet goed in hun vel en daaruit volgen vaak lichamelijke klachten als hoofdpijn en buikpijn. 
                Soms zeggen kinderen dan dat ze geen honger hebben omdat ze een beetje persoonlijke aandacht nodig hebben van de leiding. 
                Op die manier geven ze aan dat ze zich niet goed voelen en nood hebben aan iemand die luistert naar hen. 
                Luister dan ook.
            </p>

            <h2 class="text-yellow text-center text-2xl font-bold mb-4 mt-12">'Boos worden haalt niets uit'</h2>

            <p class="my-2">
                Er zijn dus meerdere oorzaken mogelijk als iemand niet wil eten, maar wat het ook is: word niet boos. 
                Of het nu een ribbel of een keti is: hen straffen omdat ze niet willen eten, haalt niks uit. 
                Kinderen gaan de aanvaring met de leiding dan associeren met het eten en zo kom je in een vicieuze cirkel terecht.
            </p>
                        
            <p class="my-2">
                Het is ook niet de bedoeling om hen te belonen als ze wél eten. 
                Moedig hen gewoon aan om toch een klein stukje te proeven. 
                Als ze dan effectief proeven, mag je dat wel sterk benadrukken en positieve aandacht geven. 
                Toon hen dat je trots bent dat ze tenminste geprobeerd hebben.
            </p>
                            
            <p class="my-2">
                Het is ook geen ramp als een kind weigert te eten en een maaltijd (of twee) overslaat, 
                dus push hen niet om te eten. Een kind dat echt honger heeft, zal uiteindelijk toch eten wat er te eten valt.
            </p>

            <h2 class="text-yellow text-center text-2xl font-bold mb-4 mt-12">Tips and Tricks</h2>

            <p class="my-2">
                Wat kan helpen om eetstress weg te nemen?
            </p>
            <p class="my-2">
                <span class="font-bold">➜ Draai de rollen om.</span>
                Bij kinderen die geen eetlust hebben om psychologische redenen ga je beter in op het probleem zelf dan te focussen op het feit dat ze niet willen eten. 
                Wat kan helpen, is het kind zelf een zorgfiguur te laten zijn. 
                Vraag hem of haar om voor je knuffel te zorgen. Zeg dat je knuffel triest is, 
                en dat je als leiding niet genoeg tijd hebt om hem te troosten. 
                De knuffel wil ook niet eten en kan niet goed slapen en wil het liefste naar huis. 
                Op die manier geef je het kindje een verantwoordelijkheidsgevoel, en door de knuffel op te beuren, 
                zal het zich zelf weer beter in zijn of haar vel voelen.
            </p>

            <p class="my-2">
                <span class="font-bold">➜ Laat de kookploeg het kampmenu op voorhand bekend maken.</span> 
                Kinderen kunnen zich dan voorbereiden op de maaltijd. 
                Voor de meesten werkt dat en wordt het eten zelf minder een probleem.
            </p>
            
            <p class="my-2">
                ➜ Aan het begin van het kamp kun je met je afdeling <span class="font-bold">een informatief spel spelen</span> over het eten op kamp. 
                Zo komen de kinderen spelenderwijs te weten wat de regels zijn, wat er zoal gegeten wordt, 
                en hoe daarmee wordt omgegaan.
            </p>
                        
            <p class="my-2">
                ➜ Bij de kleinste afdelingen kun je ook werken met <span class="font-bold">twee of drie bonnetjes</span> die ze mogen inzetten als joker als ze iets niet lusten en gewoon een boterham willen eten. 
                Op die manier hoef je de kinderen niet te <span class="italic">pushen</span> om iets te eten en kunnen ze zelf nadenken wanneer ze de bonnetjes gebruiken. 
                (Onze ervaring leert dat kinderen de bonnetjes meestal niet inzetten, omdat ze het gevoel hebben dat ze niets ‘moeten’.)
            </p>

            <p class="my-2">
                ➜ Het kan ook helpen om het eten creatief te presenteren en het is belangrijk om <span class="font-bold">de borden niet te vol te scheppen</span>, 
                want dat werkt afschrikwekkend. 
                Schep een klein beetje van alles op hun bord, het is beter als ze zelf nog extra komen vragen.
            </p>

            <h2 class="text-yellow text-center text-2xl font-bold mb-4 mt-12">Eetproblemen</h2>

            <p class="my-2">
                Soms is de onderliggende oorzaak van ‘ik lust dat niet’ een eetprobleem. 
                Als je ervan op de hoogte bent dat een lid een eetstoornis heeft, 
                is het belangrijk om met hem of haar in dialoog te gaan en samen te bespreken hoe de eetmomenten zullen verlopen. 
                Jongeren voelen zich beter in hun vel als ze voelen dat ze een stem hebben. 
                Ga dus samenzitten met het lid, en bespreek ook of jullie de rest van de afdeling op de hoogte brengen of niet. 
                Hou er wel rekening mee dat als de rest van de afdeling niet op de hoogte is, 
                ze misschien het gevoel zullen krijgen dat het lid in kwestie een voorkeursbehandeling krijgt. 
                Vermijd dat.
            </p>
                        
            <p class="my-2">
                Als je niet op de hoogte bent, maar je hebt wel een vermoeden dat er iets meer aan de hand is, 
                dan kun je dat na het kamp aan de ouders melden. 
                Gebruik daarbij zeker niet de term ‘eetstoornis’ – 
                die term wordt vaak als zeer negatief en zwaar beladen beschouwd en het is niet aan jullie om een dergelijk probleem vast te stellen. 
                Met de ouders over je bezorgdheid over de eetgewoonten praten, kan wel zeker.
            </p>
            
            <p class="my-2">
                En ten slotte nog dit: er is geen perfect afgebakende manier van handelen en elk kind is anders. 
                Het is vooral belangrijk om te praten met je leden en aan te voelen welke manier het beste werkt voor welk kind. 
