			<h1 class="text-yellow text-4xl font-bold text-center my-8">Eetproblemen op kamp</h1>
			  
            <p class="text-grey text-center my-8">29 juni 2017, door <span class="font-bold text-yellow">Liesbeth Desmet</span></p>

            <p class="my-2 italic">
                Binnenkort vertrekken je kinderen misschien op kamp? Zijn je oogappels sowieso moeilijke eters? Hoe zal dit dan verlopen op kamp? <br />
                Charlotte van Dubbelpunt, het maandblad van de Chiroleiding, interviewde één van onze kinderpsychologen over omgaan met eetproblemen op kamp! <br>
                <a class="text-blue" href="https://chiro.be/info-voor-leiding/uitgaven/tijdschriften/dubbelpunt">Met dank aan Dubbelpunt</a><br />
