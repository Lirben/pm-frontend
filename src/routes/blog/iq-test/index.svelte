                </span>
            </p>

            <h2 class="text-yellow text-center text-2xl font-bold mb-4 mt-12">Wanneer wordt een intelligentie test afgenomen bij kinderen?</h2>

            <p class="my-2">
                De redenen zijn heel divers:
        
                <span class="block"><span class="text-yellow">&#x2022;</span> Op vraag van een logopedist</span>
                <span class="block"><span class="text-yellow">&#x2022;</span> Bij zorgvragen rond frustratie op school in combinatie met goede punten</span>
                <span class="block"><span class="text-yellow">&#x2022;</span> Bij vermoeden van hoogbegaafdheid</span>
                <span class="block"><span class="text-yellow">&#x2022;</span> Bij vermoeden van een leerstoornis of een ontwikkelingsstoornis</span>
                <span class="block"><span class="text-yellow">&#x2022;</span> Om bepaalde sterktes en zwaktes bij het kind vast te stellen</span>
            </p>
            
            <h2 class="text-yellow text-center text-2xl font-bold mb-4 mt-12">Waaruit bestaat een intelligentie test?</h2>
            
            <p class="my-2">
                Bij praktijk mobiel gebruiken we enkel erkende, gestandaardiseerde testen. 
                Voor kinderen van 2 jaar tot 6 jaar gebruiken we de <a class="font-bold text-blue" href="http://www.pearsonclinical.be/wppsi-iii-nl-wechsler-preschool-primary-scale-intelligence">WIPPSI-III</a>. 
                Voor kinderen van 6 jaar tot 17 jaar maken we gebruik van de <a class="font-bold text-blue" href="https://www.pearsonclinical.be/complete-set-wisc-v-nl">WISC-V</a>. 
                Voor niet talige kinderen van 4 jaar tot 12 jaar maken we gebruik van de <a class="font-bold text-blue" href="https://www.pearsonclinical.be/wnv-nl-wechsler-non-verbal">WNV</a>.
            </p>       

            <p class="my-2">
                De intelligentietest zelf bestaat uit een verbaal en een performaal gedeelte. 
                De performale opdrachten meten inzicht in patronen, redeneren en conceptualiseren. 
                De verbale opdrachten brengen informatieverwerking, woordenschat, begrijpend luisteren en verbaal redeneren in kaart. 
                Daarnaast wordt ook de verwerkingsnelheid van de proefpersoon ingeschat.
            </p>

            <p class="my-2">
                De som van het performale IQ en het verbale IQ vormen het totale IQ. 
                Gratis IQ-testen op het internet focusen meestal enkel op het performale aspect van het IQ. 
                We zien ook vaak mensen in onze praktijk die focusen op het performale. 
                'Ons kind is goed in wiskunde en wetenschappen'. Terwijl het verbale minstens even belangrijk is. 
            </p>

            <p class="my-2">
                De verschillende subtesten meten een verschillend aspect van de intelligentie. 
                Zo krijgt de psychologe een zicht op de sterkere en zwakkere facetten van het kind. 
                Op basis hiervan kunnen er maatregelen genomen worden op school of kan verdere studiebeleiding gericht opgestart worden.
            </p>

            <p class="my-2">
                Naast de inhoud van de intelligentietest observeert de kinderpsycholoog ook het kind tijdens de afname van de test. 
                Werkhouding, concentratievermogen, motivatie en zelfs faalangst kan op die manier een stuk in kaart gebracht worden. 
                Ook dit wordt in het verslag verwerkt dat wij altijd voorzien bij het afnemen van een intelligentietest.
            </p>

            <p class="my-2">
                <span class="font-bold">Tot slot nog even dit:</span> het menselijk intellect is zoveel meer dan enkel het IQ. 
                Ook emotioneel aanvoelen, muzikaal zijn, goed kunnen tekenen zijn uitingen van intelligentie die door een IQ-test niet worden gemeten.
            </p>
