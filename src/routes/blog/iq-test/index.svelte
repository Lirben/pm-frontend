			<h1 class="text-yellow text-4xl font-bold text-center my-8">Een IQ-test, wat en waarom?</h1>

            <p class="text-grey text-center my-8">18 februari 2018, door <span class="font-bold text-yellow">Liesbeth Desmet</span></p>

            <p class="my-2 font-bold">
                Jonas* (7 jaar) aart al even niet meer op school. Hij heeft vaak ruzie met klasgenootjes en hij wordt vaak gestraft door de juf wegens slecht gedrag. 
                De initiële hulpvraag van de ouders van Jonas was dus een vraag rond <a class="text-blue" href="/Doelgroep/6-12#driftbuien">woede</a>. 
                Via een <a class="text-blue" href="/Werkwijze">belevingsonderzoek</a> kwam onze kinderpsychologe te weten dat Jonas zich heel hard verveelde op school. Dit vertaalde zich in het zoeken van negatieve aandacht.
                
                <span class="block my-2">Er was dus een vermoeden van hoogbegaafdheid en om uit te sluiten of dit de oorzaak van de hulpvraag kon zijn nam onze kinderpsychologe een IQ-test af.</span>

                <span class="block font-normal italic my-2">(* Jonas is een fictieve naam)</span>
            </p>

            <h2 class="text-yellow text-center text-2xl font-bold mb-4 mt-12">Wat is een IQ-test?</h2>
                        
            <p class="my-2">
                Een IQ-test peilt naar het <span class="font-bold">I</span>ntelligentie <span class="font-bold">Q</span>uotient (of IQ) van de persoon die de test ondergaat. 
                De Quotient in het IQ betekent dat het cijfer eigenlijk een verhouding is ten opzichte van een een groep anderen. 
                Bij de gestandaardiseerde IQ testen die wij bij Praktijk Mobiel gebruiken zijn dat steeds de leeftijdsgenootjes. 
                Het gemiddelde van de groep is bepaald op 100.
                <br />
                Een intelligentietest en het bijhorende cijfer is een momentopname. 
                Ziekte of vermoeidheid kunnen het IQ-cijfer rechtstreeks beïnvloeden. 
                Maar ook doorheen de tijd kan een IQ-cijfer van een persoon wijzigen.
                <br />
                Daarom meldt onze kinderpsycholoog steeds in welk interval uw kind valt. 
                Bijvoorbeeld: Indien Jonas een IQ-cijfer van 105 heeft, valt hij eigenlijk in het interval 96-110. 
                Indien Jonas de IQ-test op een ander moment had afgenomen, zou zijn IQ-cijfer hoogstwaarschijnlijk tussen 96 en 110 gevallen zijn.
            </p>

            <p class="my-2">
                IQ-cijfers die tussen 90 en 110 vallen, worden beschouwd als gemiddeld. 
                De onderstaande curve (bron: <a class="text-blue" href="https://nl.wikipedia.org/wiki/Intelligentiemeting">Wikipedia</a>) visualiseert mooi dat 68.2% van de leeftijdsgenoten een IQ tussen 85 en 115 hebben.
                <br />
