			<h1 class="text-yellow text-4xl font-bold text-center my-8">Vanaf welke leeftijd mag je je kind alleen thuis laten?</h1>

            <p class="text-grey text-center my-8">15 april 2019, door <span class="font-bold text-yellow">Liesbeth Desmet</span></p>

            <p class="my-2 mb-8 font-bold">
                Kinderpyschologe Liesbeth Desmet van Praktijk Mobiel geeft duiding bij het nieuws op TV oost. 
                Een lokale nieuwszender die zich richt op Oost-Vlaanderen met name regio Aalst, Dendermonde, Geraardsbergen, Lokeren, Ninove, Sint-Niklaas en Zottegem. 
                Dit interview kwam tot stand naar aanleiding van een brand in Aalst waarbij de brandweer ontdekte dat er een aantal kinderen van 6 jaar alleen thuis werden gelaten door de ouders.
            </p>

            <p class="my-2">
                <span class="block font-bold">Vanaf waneer mag je een kind alleen thuis laten?</span>
                Volgens Liesbeth Desmet, kinderpsychologe bij Praktijk Mobiel, hangt dit sterk af van de verantwoordelijkheidszin van het kind, maar voor de meeste kinderen kan dit vanaf 10 a 12 jaar. 
                Onder de leeftijd van 10 jaar adviseert Liesbeth om kinderen best niet alleen thuis te laten. 
                Indien je uw kind alleen thuis wil laten, bouw je dit best ook op. 
                Trek er dus niet meteen een halve dag op uit, maar begin met een uurtje en bouw zo stelselmatig op.
            </p>

            <p class="my-2">
                <span class="block font-bold">Hoelang mag je een kind alleen thuis laten?</span>
                Ook dit hangt af van kind tot kind, maar best niet langer dan 2 a 3 uur afhankelijk van het tijdstip van de dag. 
                Wij adviseren om kinderen pas vanaf 16 jaar een nacht alleen thuis te laten.
            </p>

